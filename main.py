# exercise 3: Adding a number to a float
new_num = int(input("Please enter a new number: "))
print(new_num + .5)

# exercise 4: Adding two floats
num1 = float(input("Please enter a number with a decimal: "))
num2 = float(input("Please enter second number with a decimal: "))
print(f"The sum is {num1 + num2}")

# exercise 5: Multiplying Floats
deci1 = float(input("Please enter a float: "))
